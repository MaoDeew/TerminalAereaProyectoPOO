    public void almacenarReserva() {
        FileOutputStream fs;
        ObjectOutputStream os;
        try {

            fs = new FileOutputStream(archivoReservas);
            os = new ObjectOutputStream(fs);

            os.writeObject(listadoReservas);

        } catch (FileNotFoundException ex) {
        } catch (IOException ex) {

        }

    }

    private void salir() {

        int respuesta = JOptionPane.showConfirmDialog(null, "Quiere hacer otra reserva?", "Confirm",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        switch (respuesta) {
            case JOptionPane.NO_OPTION:
                dispose();
                break;
            case JOptionPane.YES_OPTION:
                break;
            case JOptionPane.CLOSED_OPTION:
                break;
            default:
                break;
        }
       
    }

    public HashMap<Vuelo, Reserva> getListadoReservas() {
        return listadoReservas;
    }

