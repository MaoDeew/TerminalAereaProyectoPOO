    private void botonGenerarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGenerarReservaActionPerformed
        // TODO add your handling code here:
        String nombre = campoNombre.getText();
        String apellido = campoApellido.getText();
        String cedula = campoCedula.getText();

        Pasajero pasajero = new Pasajero(nombre, apellido, cedula);
        String tipoPago = comboTipoPago.getSelectedItem().toString();
        String puesto = campoPuestoSeleccionado.getText();
        
        if (nombre.equals("") || apellido.equals("") || cedula.equals("") || puesto.equals("")) {
            JOptionPane.showMessageDialog(null, "No deje los campos vacios");
        }else{
        
            if (puesto.equals("E1") || puesto.equals("E2") || puesto.equals("E3") || puesto.equals("E4")
                || puesto.equals("E5") || puesto.equals("E6") || puesto.equals("E7") || puesto.equals("E8")) {

            Reserva reservaEjecutivo = new ReservaEjecutivo(vueloReserva, pasajero, puesto, tipoPago, vueloReserva.getPrecioClaseEjecutiva());

            if (puesto.equals("E1")) {

                listadoReservasEjecutivo[0][0] = listadoReservas.put(vueloReserva, reservaEjecutivo);
                almacenarReserva();
                JOptionPane.showMessageDialog(null, "Reserva generada");
                botonE1.setEnabled(false);
                salir();

            }

            if (puesto.equals("E2")) {

                listadoReservasEjecutivo[0][1] = listadoReservas.put(vueloReserva, reservaEjecutivo);
                almacenarReserva();
                JOptionPane.showMessageDialog(null, "Reserva generada");
                botonE2.setEnabled(false);
                salir();

            }

        } else {

            Reserva reservaEconomico = new ReservaEconomico(vueloReserva, pasajero, puesto, tipoPago, vueloReserva.getPrecioClaseEconomica());

        }
            
        }

        

    }//GEN-LAST:event_botonGenerarReservaActionPerformed

    public final void cargarReservas(String codigoVuelo) {

        for (Map.Entry<Vuelo, Reserva> entry : listadoReservas.entrySet()) {
            Reserva reserva = entry.getValue();

            if (reserva.getVueloPasajero().getCodigoVuelo().equals(codigoVuelo)) {

                String numeroPuesto = reserva.getNumeroPuesto();

                if (numeroPuesto.equals("E1")) {
                    botonE1.setEnabled(false);
                }
                if (numeroPuesto.equals("E2")) {
                    botonE2.setEnabled(false);
                }
                if (numeroPuesto.equals("E3")) {
                    botonE3.setEnabled(false);
                }
                if (numeroPuesto.equals("E4")) {
                    botonE4.setEnabled(false);
                }
                if (numeroPuesto.equals("E5")) {
                    botonE5.setEnabled(false);
                }
                if (numeroPuesto.equals("E6")) {
                    botonE6.setEnabled(false);
                }
                if (numeroPuesto.equals("E7")) {
                    botonE7.setEnabled(false);
                }
                if (numeroPuesto.equals("E8")) {
                    botonE8.setEnabled(false);
                }

            }

        }

    }

    public final void leerReservas() {
        try {
            FileInputStream fos = new FileInputStream(archivoReservas);
            ObjectInputStream ois = new ObjectInputStream(fos);
            listadoReservas = (HashMap<Vuelo, Reserva>) ois.readObject();
            ois.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error Leyendo, aún no existe el archivo: reservas.res");

        }
    }

